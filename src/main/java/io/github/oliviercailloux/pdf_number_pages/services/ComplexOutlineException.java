package io.github.oliviercailloux.pdf_number_pages.services;

public class ComplexOutlineException extends Exception {

	public ComplexOutlineException() {
		super();
	}

	public ComplexOutlineException(String message) {
		super(message);
	}

	public ComplexOutlineException(String message, Throwable cause) {
		super(message, cause);
	}

	public ComplexOutlineException(Throwable cause) {
		super(cause);
	}

}
